public class Array2DIndex {

    private int indexOne;
    private int indexTwo;

    public Array2DIndex(int indexOne, int indexTwo) {
        this.indexOne = indexOne;
        this.indexTwo = indexTwo;
    }

    public int getIndexOne() {
        return indexOne;
    }

    public int getIndexTwo() {
        return indexTwo;
    }

}

